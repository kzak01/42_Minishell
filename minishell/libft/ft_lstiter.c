#include "libft.h"

/**
 * @brief Iterates the list ’lst’ and applies the function
’f’ on the content of each node.
 * 
 * @param lst 
 * @param (*f) function
 */
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst == NULL)
		return ;
	while (lst != NULL)
	{
		f(lst->content);
		lst = lst->next;
	}
}
